import fs from 'node:fs/promises';
import path from 'node:path';

const DIRECTUS_URL = (process.env.VITE_DIRECTUS_URL || '').replace(/\/$/, '');
const DIRECTUS_TOKEN = process.env.VITE_DIRECTUS_PUBLIC_TOKEN || '';

const outPath = path.resolve(process.cwd(), 'client/content/generated-pages.ts');

function authHeaders() {
  const headers = { 'Content-Type': 'application/json' };
  if (DIRECTUS_TOKEN) headers.Authorization = `Bearer ${DIRECTUS_TOKEN}`;
  return headers;
}

function parseProps(value) {
  if (value == null) return {};
  if (typeof value === 'object') return value;
  if (typeof value === 'string') {
    try { return JSON.parse(value); } catch { return {}; }
  }
  return {};
}

async function fetchPublishedPages() {
  if (!DIRECTUS_URL) {
    console.warn('[cms:generate] VITE_DIRECTUS_URL missing; writing empty generated pages.');
    return [];
  }

  const params = new URLSearchParams({
    filter: JSON.stringify({ status: { _eq: 'published' } }),
    limit: '-1',
    sort: 'slug',
    fields: 'id,slug,title,status,seo,sections.id,sections.sort,sections.type,sections.props',
    deep: JSON.stringify({ sections: { _sort: 'sort' } }),
  });

  const res = await fetch(`${DIRECTUS_URL}/items/pages?${params.toString()}`, {
    headers: authHeaders(),
  });

  if (!res.ok) {
    const body = await res.text();
    throw new Error(`[cms:generate] Failed fetching pages (${res.status}): ${body}`);
  }

  const json = await res.json();
  const pages = Array.isArray(json?.data) ? json.data : [];

  return pages.map((page) => ({
    id: page.id,
    slug: page.slug,
    title: page.title,
    status: page.status,
    seo: page.seo ?? null,
    sections: (page.sections || [])
      .map((section) => ({
        id: section.id,
        sort: section.sort ?? 0,
        type: section.type,
        props: parseProps(section.props),
      }))
      .sort((a, b) => (a.sort ?? 0) - (b.sort ?? 0)),
  }));
}

async function main() {
  const pages = await fetchPublishedPages();

  const content = `/* AUTO-GENERATED by scripts/generate-cms-content.mjs */\n` +
`export type GeneratedPage = {\n` +
`  id: string | number;\n` +
`  slug: string;\n` +
`  title?: string;\n` +
`  status?: string;\n` +
`  seo?: Record<string, any> | null;\n` +
`  sections: Array<{ id?: string | number; sort?: number; type?: string; props?: Record<string, any> }>;\n` +
`};\n\n` +
`export const GENERATED_PAGES: GeneratedPage[] = ${JSON.stringify(pages, null, 2)};\n`;

  await fs.mkdir(path.dirname(outPath), { recursive: true });
  await fs.writeFile(outPath, content, 'utf8');
  console.log(`[cms:generate] wrote ${pages.length} published page(s) to ${path.relative(process.cwd(), outPath)}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
